package uk.ac.cam.bms53.prejava.demo;

class PrimitiveInt
{
    public static void main(String[] args)
    {
        int i;                  // create a variable called i
        i = 1;                  // update i with the value 1
        i = i + 1;              // update i with the value i + 1
        i = 2147483624 + 40;
        System.out.println(i);  // print the current value of i
    }
}